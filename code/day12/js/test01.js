// ▶ 제어문자(Escape Character)

// \n : 문자열 안에서 줄바꿈
console.log("첫번째 줄\n 두번째 줄");

// \t : 문자열 안에서 일정간격 띄우기(tab키 정도의 간격)
console.log("tab키를 누르면\t일정간격이 띄어진다.");

// \" : 문자열 안에서 큰따옴표 표현
console.log("제 이름은 \"권소현\" 입니다.");

// \' : 문자열 안에서 작은따옴표 표현
console.log("제 이름은 \'권소현\' 입니다.");

// \\ : 문자열 안에서 역슬래시 표현
console.log("제어문자는 \\로 사용합니다.");