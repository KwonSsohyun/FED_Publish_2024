// ▶ JavaScript 자료형(Data Type)
console.log(10, typeof 10); // number
console.log(3.14, typeof 3.14); // number
console.log("문자열", typeof "문자열"); // string
console.log("10", typeof "10"); // string


let num1 = 10;
console.log(num1, typeof num1); // number


let var1;
console.log(var1, typeof var1); // undefined


let var2 = null; // 변수안에 값을 비워두고 싶을때 의도적으로 null값을 넣어준다.
console.log(var2, typeof var2); // object
