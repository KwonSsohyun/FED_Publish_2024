// ▶ 대입 연산자
let num1 = 0;
let num2 = 100;
let num3 = 5;
let num4 = 1000;

// +=
num1 += 10; // 10
num1 += 10; // 20
num1 += 10; // 30
console.log("대입 연산자 += " + num1);  // 30

// -=
num2 -= 20; // 80
num2 -= 20; // 60
num2 -= 20; // 40
console.log("대입 연산자 -= " + num2);  // 40

// *=
num3 *= 5;  // 25
num3 *= 5;  // 125
num3 *= 5;  // 625
console.log("대입 연산자 *= " + num3);  // 625

// /=
num4 /= 10; // 100
num4 /= 10; // 10
num4 /= 10; // 1
console.log("대입 연산자 /= " + num4);  // 1