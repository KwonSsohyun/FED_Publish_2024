//    조건문 - if문

console.log("=================== 조건식이 참을 반환하는 경우 ===================");

if(true){   // 영역안의 코드를 무조건 실행시킨다는 의미
    console.log("if문 안의 문장");
}
console.log("if문 바깥의 문장");

console.log("=================== 조건식이 거짓을 반환하는 경우 ===================");
if(false){   // 영역안의 코드를 무조건 실행시키지 않겠다는 의미
    console.log("if문 안의 문장");
}
console.log("if문 바깥의 문장");